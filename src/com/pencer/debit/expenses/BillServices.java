    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(datejTextField.getText()==null||datejTextField.getText().length()<1){
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify the Date when Services were rendered.",
                    "Date Missing",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(receiptjTextField.getText()==null||receiptjTextField.getText().length()<1){
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify the Receipt No..",
                    "Receipt No. Missing",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }else if(outletjTextField.getText()==null||outletjTextField.getText().length()<1){
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify the Debit Stream.",
                    "Debit Stream Missing",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            
        }else{
            boolean rowComplete=true;
            
            for(int r=0;r<jTable1.getRowCount();r++){
                for(int c=0;c<jTable1.getColumnCount();c++){
                
                    if(!(jTable1.getValueAt(r, 1)==null)){
                        if(jTable1.getValueAt(r, c)==null){
                            rowComplete=false;
                        }
                    }
                    
                }
            }
            
            if(rowComplete){
            
                try{
                    connection.setAutoCommit(false);
                    java.sql.PreparedStatement savePS=connection.prepareStatement("INSERT INTO services_bought (outlet_code, receipt_no, buy_date, service_code, qty, price, totals, description) VALUES (?,?,?,?,?,?,?,?)");
                
                    java.lang.String outletCode=null;
                    java.sql.Statement stt=connection.createStatement();
                    java.sql.ResultSet rset=stt.executeQuery("SELECT code FROM service_points WHERE description ='"+outletjTextField.getText()+"'");
                
                    while(rset.next()){
                        outletCode=rset.getString(1);
                    }
                
                    for(int r=0;r<jTable1.getRowCount();r++){
                        if(!(jTable1.getValueAt(r, 1)==null)){
                            savePS.setString(1, outletCode);
                            savePS.setString(2, receiptjTextField.getText());
                            savePS.setObject(3, datejTextField.getText());
                            savePS.setObject(4, jTable1.getValueAt(r, 0));
                            savePS.setObject(5, jTable1.getValueAt(r, 2));
                            savePS.setObject(6, jTable1.getValueAt(r, 3));
                            savePS.setObject(7, jTable1.getValueAt(r, 4));
                            savePS.setObject(8, jTable1.getValueAt(r, 5).toString().toUpperCase());
                        
                            savePS.executeUpdate();
                        }
                    }
                
                    connection.commit();
                    connection.setAutoCommit(true);
                
                    javax.swing.JOptionPane.showMessageDialog(this, "Services Data Saved!",
                        "Insert Successful.",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);

                
                    jButton2.doClick();
                
                }catch(java.sql.SQLException sqle){
                    System.err.println("Error commiting.");
                    sqle.printStackTrace();
                
                    try{
                    
                        connection.rollback();
                        connection.setAutoCommit(true);
                    
                    }catch(java.sql.SQLException sqlex){
                        System.err.println("Error rolling back.");
                        sqlex.printStackTrace();
                    }
                }
            
            
            }else{
                javax.swing.JOptionPane.showMessageDialog(this, "Please ensure all the rows are filled to completion \n"
                        + "before attempting to save",
                        "Error Message.",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
          
            
        }
          
            
                
        
    }//GEN-LAST:event_jButton1ActionPerformed

