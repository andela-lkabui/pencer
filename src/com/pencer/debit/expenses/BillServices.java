    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(jTable1.getSelectedColumn()==0){
            
            sd=new com.pencer.search.SearchDialog(true, connection, "SELECT service_description FROM services ",
                                                " WHERE service_description LIKE '%",
                                                "%' ORDER BY service_description");
            

            
            sd.addSearchDialogListener(new javax.swing.event.ListSelectionListener(){
                int selectedIndex=-1;
                
                public void valueChanged(javax.swing.event.ListSelectionEvent lse){
                    
                    selectedIndex=sd.getSearchJTable().getSelectionModel().getMinSelectionIndex();
                    
                    java.lang.String desc=(String)sd.getSearchJTable().getValueAt(selectedIndex, 0);
                    java.lang.String code=null;
                    java.lang.String quantity=null;
                    
                    jTable1.setValueAt(desc, jTable1.getSelectedRow(), 1);
                    
                    try{
                        java.sql.Statement codeStt=connection.createStatement();
                        java.sql.ResultSet codeRset=codeStt.executeQuery("SELECT code,'1' FROM services WHERE service_description='"+desc+"'");
                        
                        while(codeRset.next()){
                            code=codeRset.getString(1);
                            quantity=codeRset.getString(2);
                        }
                    }
                    catch(java.sql.SQLException sqle){
                        sqle.printStackTrace();
                    }
                    
                    jTable1.setValueAt(code, jTable1.getSelectedRow(), 0);
                    jTable1.setValueAt(quantity, jTable1.getSelectedRow(), 2);
                    
                    sd.dispose();
                }
            });
            
            sd.setVisible(true);
                        
        }
    }//GEN-LAST:event_jTable1MouseClicked

